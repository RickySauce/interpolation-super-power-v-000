def display_rainbow(colors = ["red", "orange", "yellow", "green", "blue",
    "indigo", "violet"])
  puts "the colors of the rainbow are R: #{colors[0]}, O: #{colors[1]},
        Y: #{colors[2]}, G: #{colors[3]}, B: #{colors[4]}, I: #{colors[5]},
        and V: #{colors[6]}"
end
